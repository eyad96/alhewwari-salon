-- 5. توليد المواعيد المتاحة تلقائياً للأيام الـ 8 القادمة (Generate available slot appointments for the next 8 days)
INSERT INTO public.available_slots (date, time)
SELECT 
  (current_date + i)::date,
  t::time
FROM 
  generate_series(0, 7) AS i,
  (VALUES 
    ('10:00:00'), ('11:00:00'), ('12:00:00'), ('13:00:00'), 
    ('14:00:00'), ('15:00:00'), ('16:00:00'), ('17:00:00'), 
    ('18:00:00'), ('19:00:00'), ('20:00:00')
  ) AS hours(t)
ON CONFLICT DO NOTHING;
